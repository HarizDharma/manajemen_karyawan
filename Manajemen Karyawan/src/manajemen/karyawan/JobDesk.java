    @Override
    public void tampilData() {
            Connection con = Koneksi.getCon();
         
        try {
              sql = "SELECT * FROM `job_desk` INNER JOIN pegawai on job_desk.id_pegawai = pegawai.id_pegawai"
                      + " where job_desk.id_job = '"+txtidjob.getText()+"'";
            st=con.createStatement();
            rs= st.executeQuery(sql);
            
          
            while(rs.next()){
                txtidpegawai.setText(rs.getString("id_pegawai"));
                txtnamajob.setText(rs.getString("NamaJob"));
                txtnamapegawai.setText(rs.getString("NamaPeg"));
                jtanggal.setDate(rs.getDate("tanggal"));
                txtsupervisor.setText(rs.getString("supervisor"));
    
         
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, e,"Pesan Kesalahan", JOptionPane.ERROR_MESSAGE);
        }
    }

