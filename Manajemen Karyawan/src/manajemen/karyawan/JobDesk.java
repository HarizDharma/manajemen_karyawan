    private void tampilTabel(){
        String tugas ;
            DefaultTableModel tb = new DefaultTableModel();
            tb.addColumn("Id Job");
            tb.addColumn("Id Pegawai");
            tb.addColumn("Nama Pegawai");
            tb.addColumn("Nama Job");
            tb.addColumn("Tanggal");
            tb.addColumn("Supervisor");
            tb.addColumn("Status");
 
            try {
            con = Koneksi.getCon();
            sql = "SELECT * FROM `job_desk` INNER JOIN pegawai on job_desk.id_pegawai "
                    + "= pegawai.id_pegawai";
            st = con.createStatement();
            rs = st.executeQuery(sql);
            tb.setRowCount(0);
            int i = 1;
            while (rs.next()){
                Object[] data = new Object[7];
                data [0] = rs.getString("id_job");
                data [1] = rs.getString("id_pegawai");
                data [2] = rs.getString("NamaPeg");
                data [3] = rs.getString("NamaJob");
                data [4] = rs.getDate("tanggal");
                data [5] = rs.getString("supervisor");
                       String a = rs.getString("StatusJob");
                int b = Integer.parseInt(a);
                if(b == 1){
                       tugas = "Selesai";
                }else{
                     tugas = "Belom Selesai";
                
                }
                data [6] = tugas;
                tb.addRow(data);
                i++;
            }
            tabeljobdesk.setModel(tb);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e, "TERJADI ERROR INPUT DATA", JOptionPane.ERROR_MESSAGE);
        }
    }
