    public void tampilData() {
        DefaultTableModel DTM = new DefaultTableModel();
        
        DTM.addColumn("Id");
        DTM.addColumn("Todo List");
        DTM.addColumn("Jam Mulai");
        DTM.addColumn("Jam Akhir");
        
        con = Koneksi.getCon();
        try {
            int row = Tb1.getSelectedRow();
            String IdJob = Tb1.getModel().getValueAt(row, 0).toString();
            System.out.println(IdJob);
            String sql="SELECT to_do_list_job.id_to_do_list, to_do_list_job.todolist, to_do_list_job.jam_batas_mulai, to_do_list_job.jam_batas_akhir FROM to_do_list_job WHERE to_do_list_job.id_job='"+ IdJob +"'";
            st = con.createStatement();
            rs = st.executeQuery(sql);
            DTM.setRowCount(0);
            int i = 1;
            while (rs.next()) {
                
                Object[] data = new Object[4];
                data[0] = rs.getString("id_to_do_list");
                data[1] = rs.getString("todolist");
                data[2] = rs.getString("jam_batas_mulai");
                data[3] = rs.getString("jam_batas_akhir");
                DTM.addRow(data);
                i++;
            }
            ListTable.setModel(DTM);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e, "Kesalahan Tampil Todo List !", JOptionPane.ERROR_MESSAGE);
        }
    }

