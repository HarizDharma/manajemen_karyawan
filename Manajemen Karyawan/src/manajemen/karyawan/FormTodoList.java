    public void FillListBox() {
        
        
        DefaultTableModel m = new DefaultTableModel();
        
        m.addColumn("Id");
        m.addColumn("Job Desk");
        m.addColumn("Tanggal");
        m.addColumn("Status");

        con = Koneksi.getCon();
        try {
            String tugas ;
            String sql="SELECT * FROM `job_desk` INNER JOIN pegawai ON job_desk.id_pegawai = pegawai.id_pegawai WHERE job_desk.id_pegawai ='"+ txtIdPeg.getText() +"'";
            st = con.createStatement();
            rs = st.executeQuery(sql);
            m.setRowCount(0);
     
            int i = 1;
            while (rs.next()) {
                Object[] data = new Object[4];
                data[0] = rs.getString("id_job");
                data[1] = rs.getString("NamaJob");
                data[2] = rs.getString("tanggal");
                String a = rs.getString("StatusJob");
                int b = Integer.parseInt(a);
                if(b == 1){
                       tugas = "Selesai";
                }else{
                     tugas = "Belom Selesai";
                
                }
                     data[3] = tugas;
                
                m.addRow(data);
                i++;
            }
            Tb1.setModel(m);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e, "Kesalahan Tampil Job Desk !", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    
